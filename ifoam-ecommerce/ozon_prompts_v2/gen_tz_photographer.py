def para(text, bold=False, italic=False, color=None, size=11, align=None):
    p = doc.add_paragraph()
    if align:
        p.alignment = align
    run = p.add_run(text)
    run.font.size = Pt(size)
    run.font.bold = bold
    run.font.italic = italic
    if color:
        run.font.color.rgb = color
    return p

def bullet(text, bold_prefix=None):
    p = doc.add_paragraph(style='List Bullet' if 'List Bullet' in [s.name for s in doc.styles] else 'Normal')
    if bold_prefix:
        r1 = p.add_run(bold_prefix)
        r1.font.bold = True
        r1.font.size = Pt(11)
        p.add_run(text).font.size = Pt(11)
    else:
        r = p.add_run(text)
        r.font.size = Pt(11)
    return p

def set_cell_bg(cell, hex_color):
    """Закрашивает ячейку таблицы."""
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), hex_color)
    tcPr.append(shd)

def add_table(headers, rows, col_widths=None, header_bg='2D2D2D', alt_bg='F2F2F2'):
    """Добавляет стилизованную таблицу."""
    table = doc.add_table(rows=1, cols=len(headers))
    table.style = 'Table Grid'

    # Заголовок
    hdr_row = table.rows[0]
    for i, (cell, hdr) in enumerate(zip(hdr_row.cells, headers)):
        set_cell_bg(cell, header_bg)
        p = cell.paragraphs[0]
        run = p.add_run(hdr)
        run.font.bold = True
        run.font.size = Pt(10)
        run.font.color.rgb = WHITE
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER

    # Строки данных
    for r_idx, row_data in enumerate(rows):
        row = table.add_row()
        bg = alt_bg if r_idx % 2 == 1 else 'FFFFFF'
        for i, (cell, val) in enumerate(zip(row.cells, row_data)):
            if bg != 'FFFFFF':
                set_cell_bg(cell, bg)
            p = cell.paragraphs[0]
            if isinstance(val, tuple):
                # (текст, bold, цвет)
                r = p.add_run(val[0])
                r.font.size = Pt(10)
                if len(val) > 1: r.font.bold = val[1]
                if len(val) > 2 and val[2]: r.font.color.rgb = val[2]
            else:
                r = p.add_run(str(val))
                r.font.size = Pt(10)

    # Ширины колонок
    if col_widths:
        for row in table.rows:
            for i, cell in enumerate(row.cells):
                cell.width = Cm(col_widths[i])

    doc.add_paragraph()  # отступ после таблицы
    return table

def kv_table(pairs, col_widths=(5.5, 11.0)):
    """Таблица ключ → значение."""
    table = doc.add_table(rows=0, cols=2)
    table.style = 'Table Grid'
    for i, (key, val) in enumerate(pairs):
        row = table.add_row()
        bg = 'F7F7F7' if i % 2 == 0 else 'FFFFFF'
        set_cell_bg(row.cells[0], 'EEEEEE')
        set_cell_bg(row.cells[1], bg)
        r1 = row.cells[0].paragraphs[0].add_run(key)
        r1.font.bold = True
        r1.font.size = Pt(10)
        r2 = row.cells[1].paragraphs[0].add_run(val)
        r2.font.size = Pt(10)
        row.cells[0].width = Cm(col_widths[0])
        row.cells[1].width = Cm(col_widths[1])
    doc.add_paragraph()
    return table

