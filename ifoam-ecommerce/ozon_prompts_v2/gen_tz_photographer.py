def h1(text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(14)
    p.paragraph_format.space_after  = Pt(4)
    run = p.add_run(text)
    run.font.size  = Pt(18)
    run.font.bold  = True
    run.font.color.rgb = ORANGE
    run.font.name  = "Arial"
    return p

